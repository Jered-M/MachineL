import tensorflow as tf

# 1. Charger ton mod√®le entra√Æn√© (.h5)
model = tf.keras.models.load_model("face.h5")

# 2. Convertisseur TFLite
converter = tf.lite.TFLiteConverter.from_keras_model(model)

# (OPTIONNEL mais conseill√© : quantization pour mobile)
converter.optimizations = [tf.lite.Optimize.DEFAULT]

# 3. Convertir
tflite_model = converter.convert()

# 4. Sauvegarder le mod√®le TFLite
with open("model.tflite", "wb") as f:
    f.write(tflite_model)

print("üéâ Conversion termin√©e ! Fichier g√©n√©r√© : model.tflite")
